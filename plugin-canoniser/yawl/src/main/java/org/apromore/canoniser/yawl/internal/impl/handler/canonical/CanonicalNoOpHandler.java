package org.apromore.canoniser.yawl.internal.impl.handler.canonical;

import org.apromore.canoniser.exception.CanoniserException;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * Handler for a Canonical Element that does nothing at all.
 *
 * @author <a href="mailto:felix.mannhardt@smail.wir.h-brs.de">Felix Mannhardt (Bonn-Rhein-Sieg University oAS)</a>
 *
 */
public class CanonicalNoOpHandler extends CanonicalElementHandler<Object, Object> {

    private static final Logger LOGGER = LoggerFactory.getLogger(CanonicalNoOpHandler.class.getName());

    /*
     * (non-Javadoc)
     *
     * @see org.apromore.canoniser.yawl.internal.impl.handler.ConversionHandler#convert()
     */
    @Override
    public void convert() throws CanoniserException {
        // NoOp
        LOGGER.debug("Ignoring element {}", getObject().toString());
    }

}
